package com.haleywang.putty.common;

public class IllegalAccessException extends SpringRemoteException {
    public IllegalAccessException() {
        super();
    }


    public IllegalAccessException(String message) {
        super(message);
    }


    public IllegalAccessException(String message, Throwable cause) {
        super(message, cause);
    }


    public IllegalAccessException(Throwable cause) {
        super(cause);
    }

}
